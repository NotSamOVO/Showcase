void draw_text(
    std::vector<unsigned char> & image,
    int width,
    int height,
    const std::string & text,
    int x,
    int y,
    const std::vector<unsigned char> & color,
    int scale)
{
    int cursor_x = x;
    int cursor_y = y;

    for (char c : text) {
        if (c < 32 || c > 126) c = 32; // Handle unknown chars as space
        int index = c - 32;

        for (int col = 0; col < 5; ++col) {
            unsigned char line = font5x7[index][col];
            for (int row = 0; row < 7; ++row) {
                if (line & (1 << row)) {
                    // Draw pixel at (cursor_x + col*scale, cursor_y + row*scale)
                    // Handle scaling
                    for (int sx = 0; sx < scale; ++sx) {
                        for (int sy = 0; sy < scale; ++sy) {
                            int px = cursor_x + col * scale + sx;
                            int py = cursor_y + row * scale + sy;

                            if (px >= 0 && px < width && py >= 0 && py < height) {
                                int idx = 3 * (px + width * py);
                                image[idx + 0] = color[0];
                                image[idx + 1] = color[1];
                                image[idx + 2] = color[2];
                            }
                        }
                    }
                }
            }
        }
        cursor_x += 6 * scale; // 5 width + 1 spacing
    }
}
